async function enhanceAllModels() {
  console.log('Reading munger-models.json...')
  const models = JSON.parse(fs.readFileSync(inputPath, 'utf-8'))

  let enhancedCount = 0
  const enhancedModels = models.map(model => {
    let newContent = model.content

    // 应用深度增强
    if (enhancements[model.index]) {
      newContent += enhancements[model.index].valueInvestingContext
      enhancedCount++
    }
    // 应用简短增强
    else if (shortEnhancements[model.index]) {
      newContent += shortEnhancements[model.index].context
      enhancedCount++
    }

    return {
      ...model,
      content: newContent
    }
  })

  fs.writeFileSync(outputPath, JSON.stringify(enhancedModels, null, 2), 'utf-8')
  console.log(`\nEnhanced ${enhancedCount} models`)
  console.log(`Written to ${outputPath}`)

  // 统计内容长度分布
  const lengths = enhancedModels.map(m => m.content.length)
  const avgLength = lengths.reduce((a, b) => a + b, 0) / lengths.length
  const minLength = Math.min(...lengths)
  const maxLength = Math.max(...lengths)
  const shortCount = lengths.filter(l => l < 300).length

  console.log(`\nContent length statistics:`)
  console.log(`Average: ${Math.round(avgLength)} chars`)
  console.log(`Min: ${minLength} chars`)
  console.log(`Max: ${maxLength} chars`)
  console.log(`Models with < 300 chars: ${shortCount}`)

  // 显示仍然较短的模型
  if (shortCount > 0) {
    console.log(`\nModels still needing enhancement:`)
    enhancedModels
      .filter(m => m.content.length < 300)
      .slice(0, 10)
      .forEach(m => {
        console.log(`${m.index}. ${m.title}: ${m.content.length} chars`)
      })
  }
}

enhanceAllModels().catch(console.error)
