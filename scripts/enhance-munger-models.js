Object.assign(enhancements, additionalEnhancements)

async function enhanceModels() {
  console.log('Reading munger-models.json...')
  const models = JSON.parse(fs.readFileSync(inputPath, 'utf-8'))

  let enhancedCount = 0
  const enhancedModels = models.map(model => {
    if (enhancements[model.index]) {
      enhancedCount++
      return {
        ...model,
        content: model.content + enhancements[model.index].valueInvestingContext
      }
    }
    return model
  })

  fs.writeFileSync(outputPath, JSON.stringify(enhancedModels, null, 2), 'utf-8')
  console.log(`Enhanced ${enhancedCount} models`)
  console.log(`Written to ${outputPath}`)

  // 显示增强后的前几个模型的长度
  console.log('\nEnhanced models content length:')
  enhancedModels.slice(0, 10).forEach(m => {
    console.log(`${m.index}. ${m.title}: ${m.content.length} chars`)
  })
}

enhanceModels().catch(console.error)
